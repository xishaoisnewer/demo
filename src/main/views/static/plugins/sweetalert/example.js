function check(id){
    swal(
        {title:"您确定要删除这条数据吗",
            text:"删除后将无法恢复，请谨慎操作！",
            type:"warning",
            showCancelButton:true,
            confirmButtonColor:"#DD6B55",
            confirmButtonText:"确定删除！",
            cancelButtonText:"取消",
            closeOnConfirm:false,
            closeOnCancel:false
        },
        function(isConfirm)
        {
            if(isConfirm)
            {
                swal({title:"删除成功！",
                    text:"您已经永久删除了这条数据。",
                    type:"success"},function(){window.location="/video/destroy/"+id})
            }
            else{
                swal({title:"已取消",
                    text:"您取消了删除操作！",
                    type:"error"})
            }
        }
    )
}
